#include "main.h"
#include <stdio.h>
/**
 * print_to_98 - Prints from n to 98
 *
 * Description: Prints all natural numbers form n to 98
 * @n: initial integer
 * Return: void
 */

void print_to_98(int n)
{
	if (n <= 98)
	{
		for (; n <= 98; n++)
		{
			printf("%d", n);
			if (n != 98)
			{
				printf(", ");
			}			
		}
	}
	else
	{
		for (; n >= 98; n--)
		{
			printf("%d", n);
			if (n != 98)
			{
				printf(", ");
			}
		}
	}
	printf("\n");
}
